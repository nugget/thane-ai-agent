// Command gencfg generates examples/config.example.yaml from the
// internal/config package. It extracts struct field doc comments from the
// Go source via go/ast and uses [config.ExampleConfig] for example values,
// producing a fully-documented YAML reference file that cannot drift from
// the code.
//
// Usage (via go generate in internal/config):
//
//	go run ./gen/gencfg -srcdir $GODIR -out $GODIR/../../examples/config.example.yaml
//
// Optional sections (MQTT, Signal, Forge, etc.) are emitted as commented-out
// YAML blocks so new users see the full configuration surface without
// accidentally enabling services they have not configured. The set of
// optional keys is defined by [optionalKeys].
package main

